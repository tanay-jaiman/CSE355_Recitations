#!/usr/bin/env python3

from cse355_machine_design import PDA

def pda_example():
    Q = {"q_0","q_1","q_2","q_3","q_4","q_5","q_6","q_7"}
    Sigma = {"0","1"}
    Gamma = {"0","1","$"}
    delta = {
        ("q_0", "_", "_"): {("q_1", "$")},
        ("q_1", "0", "_"): {("q_1", "0")},
        ("q_1", "1", "_"): {("q_1", "1")},
        ("q_1", "_", "_"): {("q_2", "_"), ("q_6", "_"), ("q_7", "_")},
        ("q_6", "0", "_"): {("q_3", "_")},
        ("q_7", "1", "_"): {("q_4", "_")},
        ("q_2", "0", "0"): {("q_2", "_")},
        ("q_2", "1", "1"): {("q_2", "_")},
        ("q_2", "_", "$"): {("q_5", "_")},
        ("q_3", "1", "1"): {("q_3", "_")},
        ("q_3", "0", "0"): {("q_3", "_")},
        ("q_3", "_", "$"): {("q_5", "_")},
        ("q_4", "1", "1"): {("q_4", "_")},
        ("q_4", "0", "0"): {("q_4", "_")},
        ("q_4", "_", "$"): {("q_5", "_")},
    }
    q0 = "q_0"
    F = {"q_5"}

    return PDA(Q, Sigma, Gamma, delta, q0, F)